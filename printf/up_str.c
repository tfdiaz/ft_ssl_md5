#include "ft_printf.h"

void	up_str(char **s)
{
	int i;

	i = 0;
	while ((*s)[i] != '\0')
	{
		if ((*s)[i] >= 'a' && (*s)[i] <= 'z')
			(*s)[i] -= 32;
		i++;
	}
}
