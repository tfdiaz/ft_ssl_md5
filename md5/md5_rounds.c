static unsigned int	g_smov[16] = {
	7, 12, 17, 22, 5, 9, 14, 20, 4, 11, 16, 23, 6, 10, 15, 21
};

void	round12(uint32_t *st[4], uint32_t x[16])
{
	int i;

	i = 0;
	while (i < 16)
	{
		(*st)[(64 - i) % 4] += prm_f((*st)[(64 - i + 1) % 4],
			(*st)[(64 - i + 2) % 4], (*st)[(64 - i + 3) % 4])
			+ x[i] + g_tsin[i];
		(*st)[(64 - i) % 4] = rl((*st)[(64 - i) % 4], g_smov[i % 4]);
		(*st)[(64 - i) % 4] += (*st)[(64 - i + 1) % 4];
		i++;
	}
	while (i < 32)
	{
		(*st)[(64 - i) % 4] += prm_g((*st)[(64 - i + 1) % 4],
			(*st)[(64 - i + 2) % 4], (*st)[(64 - i + 3) % 4])
			+ x[(((i % 16) * 5) + 1) % 16] + g_tsin[i];
		(*st)[(64 - i) % 4] = rl((*st)[(64 - i) % 4], g_smov[i % 4 + 4]);
		(*st)[(64 - i) % 4] += (*st)[(64 - i + 1) % 4];
		i++;
	}
}

void	round34(uint32_t *st[4], uint32_t x[64])
{
	int	i;

	i = 32;
	while (i < 48)
	{
		(*st)[(64 - i) % 4] += prm_h((*st)[(64 - i + 1) % 4],
			(*st)[(64 - i + 2) % 4], (*st)[(64 - i + 3) % 4])
			+ x[(((i % 16) * 3) + 5) % 16] + g_tsin[i];
		(*st)[(64 - i) % 4] = rl((*st)[(64 - i) % 4], g_smov[i % 4 + 8]);
		(*st)[(64 - i) % 4] += (*st)[(64 - i + 1) % 4];
		i++;
	}
	while (i < 64)
	{
		(*st)[(64 - i) % 4] += prm_i((*st)[(64 - i + 1) % 4],
			(*st)[(64 - i + 2) % 4], (*st)[(64 - i + 3) % 4])
			+ x[((i % 16) * 7) % 16] + g_tsin[i];
		(*st)[(64 - i) % 4] = rl((*st)[(64 - i) % 4], g_smov[i % 4 + 12]);
		(*st)[(64 - i) % 4] += (*st)[(64 - i + 1) % 4];
		i++;
	}
}
